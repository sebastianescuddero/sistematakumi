package net.takumi.entidades;

public class POS {
	private int codigo_pos;
	private String pos, numero_pos;
	
	public int getCodigo_pos() {
		return codigo_pos;
	}
	public void setCodigo_pos(int codigo_pos) {
		this.codigo_pos = codigo_pos;
	}
	public String getPos() {
		return pos;
	}
	public void setPos(String pos) {
		this.pos = pos;
	}
	public String getNumero_pos() {
		return numero_pos;
	}
	public void setNumero_pos(String numero_pos) {
		this.numero_pos = numero_pos;
	}
}
